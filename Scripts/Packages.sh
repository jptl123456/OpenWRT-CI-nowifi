# 检查包是否添加成功
check_package() {
    local pkg=$1
    if [ -d "$pkg" ] || find . -maxdepth 2 -type d -name "*$pkg*" | grep -q .; then
        echo "✓ $pkg 已添加"
    else
        echo "✗ $pkg 未找到"
    fi
}

